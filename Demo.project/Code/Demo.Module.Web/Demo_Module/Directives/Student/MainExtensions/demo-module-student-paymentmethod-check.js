"use strict";

app.directive("demoModuleStudentPaymentmethodCheck", ["UtilsService", "VRNotificationService", "VRUIUtilsService",
    function (UtilsService, VRNotificationService, VRUIUtilsService) {

        var directiveDefinitionObject = {

            restrict: "E",
            scope:
            {
                onReady: "=",
            },

            controller: function ($scope, $element, $attrs) {
                var ctrl = this;

                var ctor = new CheckMethod($scope, ctrl, $attrs);
                ctor.initializeController();
            },

            controllerAs: "ctrl",
            bindToController: true,
            compile: function (element, attrs) {

            },
            templateUrl: "/Client/Modules/Demo_Module/Directives/Student/MainExtensions/Templates/CheckMethodTemplate.html"
        };

        function CheckMethod($scope, ctrl, $attrs) {
            this.initializeController = initializeController;

            function initializeController() {
                $scope.scopeModel = {};
                defineAPI();
            }

            function defineAPI() {
                var api = {};

                api.load = function (payload) {
                    if (payload != undefined && payload.paymentMethodEntity != undefined) {
                        $scope.scopeModel.amount = payload.paymentMethodEntity.Amount;
                        $scope.scopeModel.checknumber = payload.paymentMethodEntity.CheckNumber;

                    }
                    var promises = [];
                    return UtilsService.waitMultiplePromises(promises);
                };

                api.getData = function () {
                    return {
                        $type: "Demo.Module.MainExtension.Student.CheckMethod,Demo.Module.MainExtension",
                        Amount: $scope.scopeModel.amount,
                        CheckNumber: $scope.scopeModel.checknumber
                    };
                };

                if (ctrl.onReady != null)
                    ctrl.onReady(api);
            }
        }

        return directiveDefinitionObject;

    }
]);