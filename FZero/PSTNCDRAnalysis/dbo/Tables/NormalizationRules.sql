CREATE TABLE [dbo].[NormalizationRules] (
    [Id]                  INT             IDENTITY (1, 1) NOT NULL,
    [Prefix]              VARCHAR (3)     NULL,
    [CallLength]          INT             NULL,
    [In_TrunckName]       VARCHAR (10)    NULL,
    [Out_TrunckName]      VARCHAR (10)    NULL,
    [Durations]           NUMERIC (38, 4) NULL,
    [CallsCount]          INT             NULL,
    [Supplement]          VARCHAR (4)     NULL,
    [Ignore]              INT             NULL,
    [Party]               VARCHAR (4)     NULL,
    [SwitchId]            INT             NULL,
    [SwitchName]          VARCHAR (50)    NULL,
    [PrefixToAdd]         VARCHAR (6)     NULL,
    [SubstringStartIndex] INT             NULL,
    [SubstringLength]     INT             NULL,
    [SuffixToAdd]         VARCHAR (5)     NULL,
    [In_TrunckId]         INT             NULL,
    [Out_TrunckId]        INT             NULL,
    CONSTRAINT [PK_NormalizationRules] PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [FK_NormalizationRules_SwitchProfiles] FOREIGN KEY ([SwitchId]) REFERENCES [dbo].[SwitchProfiles] ([Id]),
    CONSTRAINT [FK_NormalizationRules_SwitchTruncks] FOREIGN KEY ([In_TrunckId]) REFERENCES [dbo].[SwitchTruncks] ([Id]),
    CONSTRAINT [FK_NormalizationRules_SwitchTruncks1] FOREIGN KEY ([Out_TrunckId]) REFERENCES [dbo].[SwitchTruncks] ([Id])
);

